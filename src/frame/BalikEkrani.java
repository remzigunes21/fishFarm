    private void ekle_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ekle_buttonActionPerformed
        // TODO add your handling code here:

        mesaj_yazisi.setText("");

        String cins = balik_cinsi_alani.getText();
        String fiyat = fiyat_alani.getText();
        String kilo = kilo_alani.getText();
        String boy = boy_alani.getText();
        String kategori = yem_kategori_alani.getText();
        String yem_kilo = yem_kilo_alani.getText();
        String tarih = tarih_alani.getText();
        
        boolean kontrol = balikDao.kontrolYap(cins,kilo,boy,tarih);

        if (cins.equals("") && fiyat.equals("") && kilo.equals("") && boy.equals("")
                && kategori.equals("") && yem_kilo.equals("") && tarih.equals("")) {

            JOptionPane.showMessageDialog(this, "Lutfen gerekli alanları doldurunuz.");
        } else if (!kontrol) {
            JOptionPane.showMessageDialog(this, "Böyle bir kayıt zaten var.");
        }
        
        else {
            balikDao.ekleBalik(cins, fiyat, kilo, boy, kategori, yem_kilo, tarih);
            mesaj_yazisi.setText("Balık basarıyla eklendi");

        }

        balikGoruntule();
        bosalt();

    }//GEN-LAST:event_ekle_buttonActionPerformed

    private void guncelle_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guncelle_buttonActionPerformed
        // TODO add your handling code here:

        mesaj_yazisi.setText("");

        String cins = balik_cinsi_alani.getText();
        String fiyat = fiyat_alani.getText();
        String kilo = kilo_alani.getText();
        String boy = boy_alani.getText();
        String kategori = yem_kategori_alani.getText();
        String yem_kilo = yem_kilo_alani.getText();
        String tarih = tarih_alani.getText();

        int selectedRow = balik_table.getSelectedRow();

        if (selectedRow == -1) {
            if (model.getRowCount() == 0) {
                mesaj_yazisi.setText("balık tablosu suanda bos");
            } else {
                mesaj_yazisi.setText("lutfen guncellenecek bir balık seçiniz?");
            }

        } else {
            long id = (long) model.getValueAt(selectedRow, 0);
            if (true) {
                balikDao.guncelleBalik(id, cins, fiyat, kilo, boy, kategori, yem_kilo, tarih);
                mesaj_yazisi.setText("Balık basarıyla guncellendi");
            } else {
                mesaj_yazisi.setText("balık guncellenirken bir hata olustu ...lütfen tekrar deneyin..");
            }

            balikGoruntule();
            bosalt();
        }


    }//GEN-LAST:event_guncelle_buttonActionPerformed

    private void sil_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sil_buttonActionPerformed
        // TODO add your handling code here:

        int selectedRow = balik_table.getSelectedRow();

        if (selectedRow == -1) {
            if (model.getRowCount() == 0) {
                mesaj_yazisi.setText("balık tablosu suanda bos");
            } else {
                mesaj_yazisi.setText("lutfen silinecek bir balık seçiniz?");
            }

        } else {
            long id = (long) model.getValueAt(selectedRow, 0);
            if (true) {
                balikDao.silBalik(id);
                mesaj_yazisi.setText("Balık basarıyla silindi");
            } else {
                mesaj_yazisi.setText("balık silinirken bir hata olustu ...lütfen tekrar deneyin..");
            }

        }
        balikGoruntule();
        bosalt();
    }//GEN-LAST:event_sil_buttonActionPerformed
