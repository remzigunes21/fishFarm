    public void dinamikAra(String ara) {
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
        table_alani.setRowSorter(sorter);

        sorter.setRowFilter(RowFilter.regexFilter(ara));
    }
