    public void balikGoruntule() {
        model.setRowCount(0);//tabloyu sıfırlamak için

        ArrayList<Balik> balik = new ArrayList<>();

        balik = balikDao.balikList();

        if (balik != null) {
            for (Balik balik1 : balik) {
                Object[] eklenecek = {
                    balik1.getId(), balik1.getBalikCinsi(), balik1.getBalikFiyat(), balik1.getKilo(), balik1.getBoy(),
                    balik1.getYemKategori(), balik1.getYemKilo(), balik1.getGetirilisTarihi()
                };

                model.addRow(eklenecek);
            }
        } else {
            System.out.print("hata");
        }
    }

