    private void userGoruntule() {
        model.setRowCount(0);
        
        ArrayList<User> users = new ArrayList<>();
        
        users = this.getUserDao().userList();
        
        if (users != null) {
            for (User user : users) {
                Object[] eklenecek = {
                    user.getId(), user.getUserName(), user.getPassword()
                };
                model.addRow(eklenecek);
            }
        } else {
            mesaj_yazisi.setText("tablo goruntulenemiyor");
        }
    }
