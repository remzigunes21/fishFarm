    private void ekle_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ekle_buttonActionPerformed
        // TODO add your handling code here:
        mesaj_yazisi.setText("");

        String kategori = kategori_alani.getText();
        String fiyat = fiyat_alani.getText();
        String adet = yem_adet_alani.getText();
        String miktar = yem_miktar_alani.getText();
        String tarih = tarih_alani.getText();
        
         boolean kontrol = yemDao.kontrolYap(kategori,fiyat,adet,miktar,tarih);

        if (kategori.equals("") || fiyat.equals("") || adet.equals("") || miktar.equals("") || tarih.equals("")) {
            JOptionPane.showMessageDialog(this, "Lutfen gerekli alanları doldurunuz ...");
            
            return;
        } else if (!kontrol) {
            JOptionPane.showMessageDialog(this, "Boyle bir kayıt zaten var ...");
            return;
        }
        else {
            yemDao.ekleYem(kategori, fiyat, adet, miktar, tarih);
            mesaj_yazisi.setText("Yem basarıyla eklendi");
            // mesaj_yazisi.setText("Yem eklenirken bir hata olustu ...lütfen tekrar deneyin..");
        }

        yemGoruntule();
        bosalt();


    }//GEN-LAST:event_ekle_buttonActionPerformed

    private void guncelle_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guncelle_buttonActionPerformed
        // TODO add your handling code here:
        mesaj_yazisi.setText("");
        String kategori = kategori_alani.getText();
        String fiyat = fiyat_alani.getText();
        String adet = yem_adet_alani.getText();
        String miktar = yem_miktar_alani.getText();
        String tarih = tarih_alani.getText();

        int selectedRow = table_alani.getSelectedRow();

        if (selectedRow == -1) {
            if (model.getRowCount() == 0) {
                mesaj_yazisi.setText("yem tablosu suanda bos");
            } else {
                mesaj_yazisi.setText("lutfen guncellenecek bir yem seçiniz...");
            }
        } else {
            long id = (long) model.getValueAt(selectedRow, 0);
            yemDao.guncelleYem(id, kategori, fiyat, adet, miktar, tarih);
            yemGoruntule();

            mesaj_yazisi.setText("yem basarıyla guncellendi");
            bosalt();
        }
    }//GEN-LAST:event_guncelle_buttonActionPerformed

    private void sil_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sil_buttonActionPerformed
        // TODO add your handling code here:
        int selectedRow = table_alani.getSelectedRow();

        if (selectedRow == -1) {
            if (model.getRowCount() == 0) {
                mesaj_yazisi.setText("yem tablosu suanda bos");
            } else {
                mesaj_yazisi.setText("lutfen silinecek bir yem seçiniz?");
            }

        } else {
            long id = (long) model.getValueAt(selectedRow, 0);
            if (true) {
                try {
                    yemDao.silYem(id);
                } catch (SQLException ex) {
                    Logger.getLogger(YemEkrani.class.getName()).log(Level.SEVERE, null, ex);
                }
                mesaj_yazisi.setText("Yem basarıyla silindi");
            } else {
                mesaj_yazisi.setText("Yem silinirken bir hata olustu ...lütfen tekrar deneyin..");
            }

        }
        yemGoruntule();
        bosalt();
    }//GEN-LAST:event_sil_buttonActionPerformed

