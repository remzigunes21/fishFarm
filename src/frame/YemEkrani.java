    private void yemGoruntule() {
        model.setRowCount(0);

        ArrayList<Yem> yems = new ArrayList<>();

        yems = yemDao.yemListeleme();

        if (yems != null) {
            for (Yem yem : yems) {
                Object[] eklenecek = {
                    yem.getId(), yem.getYemKategori(), yem.getYemFiyat(), yem.getYemAdet(), yem.getYemMiktar(), yem.getGelmeTarihi()
                };

                model.addRow(eklenecek);
            }
        } else {
            System.out.println("hata");
        }
    }

