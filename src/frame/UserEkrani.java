    private void ekle_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ekle_buttonActionPerformed
        // TODO add your handling code here:
        mesaj_yazisi.setText("");
        
        String kullanici = kullanici_alani.getText();
        String parola = new String(parola_alani.getPassword());
        
        boolean kontrol = userDao.kontrolYap(kullanici, parola);
        
        if (kullanici.equals("") && parola.equals("")) {
            JOptionPane.showMessageDialog(this, "Lutfen gerekli alanları doldurunuz ...");
        } else if (!kontrol) {
            JOptionPane.showMessageDialog(this, "Boyle bir kullanıcı zaten var...lutfen baska kullanıcı seciniz..");
        }
        
        else {
            this.getUserDao().ekleUser(kullanici, parola);
            mesaj_yazisi.setText("User basarıyla eklendi");
            //mesaj_yazisi.setText("User eklenirken bir hata olustu ...lütfen tekrar deneyin..");
        }
        
        userGoruntule();
        bosalt();
    }//GEN-LAST:event_ekle_buttonActionPerformed

    private void guncelle_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guncelle_buttonActionPerformed
        // TODO add your handling code here:
        mesaj_yazisi.setText("");
        
        String kullanici = kullanici_alani.getText();
        String parola = new String(parola_alani.getPassword());
        
        int selectedRow = table_alani.getSelectedRow();
        
        if (selectedRow == -1) {
            if (model.getRowCount() == 0) {
                mesaj_yazisi.setText("User tablosu suanda bos");
            } else {
                mesaj_yazisi.setText("lutfen guncellenecek bir User seçiniz...");
            }
        } else {
            long id = (long) model.getValueAt(selectedRow, 0);
            this.getUserDao().guncelleUser(id, kullanici, parola);
            
            userGoruntule();
            
            mesaj_yazisi.setText("User basarıyla guncellendi");
            bosalt();
        }
    }//GEN-LAST:event_guncelle_buttonActionPerformed

    private void sil_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sil_buttonActionPerformed
        // TODO add your handling code here:
        int selectedRow = table_alani.getSelectedRow();
        
        if (selectedRow == -1) {
            if (model.getRowCount() == 0) {
                mesaj_yazisi.setText("User tablosu suanda bos");
            } else {
                mesaj_yazisi.setText("lutfen silinecek bir User seçiniz?");
            }
            
        } else {
            long id = (long) model.getValueAt(selectedRow, 0);
            if (true) {
                this.getUserDao().silUser(id);
                mesaj_yazisi.setText("User basarıyla silindi");
            } else {
                mesaj_yazisi.setText("User silinirken bir hata olustu ...lütfen tekrar deneyin..");
            }
            
        }        
        userGoruntule();
        bosalt();
    }//GEN-LAST:event_sil_buttonActionPerformed

